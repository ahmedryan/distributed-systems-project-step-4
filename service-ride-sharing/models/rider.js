
class Rider {
    constructor(rider_name, rider_coordinate_x, rider_coordinate_y,
         rider_destination_x, rider_destination_y) {
        this.rider_name = rider_name;
        this.rider_coordinate_x = rider_coordinate_x;
        this.rider_coordinate_y = rider_coordinate_y;
        this.rider_destination_x = rider_destination_x;
        this.rider_destination_y = rider_destination_y;
    }
}

module.exports = Rider;
